//Variables
//1)var
//it is a globally declared variable
var age = 20;
var num1 = 10;
var num2 = 20;

age = "udit";
console.log(age);
console.log(num1+num2);

//2)let 
//it is a locally declared variable
if(true){
let age = 20;
let num1 = 10;
let num2 = 20;

age = "udit";
console.log(age);
console.log(num1+num2);
}

//3)const
//it is a constant
//it is a locally declared variable
//it can not be redefined once its defined
const ag = 30;
